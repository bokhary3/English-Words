import UIKit
import CoreData
import SwiftyStoreKit
import Firebase
import MOLH

@UIApplicationMain
class AppDelegate: UIResponder, UIApplicationDelegate {

    var window: UIWindow?


    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplicationLaunchOptionsKey: Any]?) -> Bool {
        handleRateAlert()
        completeStorKitTransaction()
        setup()
        
        MOLH.shared.activate(true)
        MOLH.shared.specialKeyWords = ["cancel" ,"Done", "Back"]
        var black = UIColor.black
        if #available(iOS 13, *) {
            black = .label
        }
        UINavigationBar.appearance().titleTextAttributes = [.foregroundColor: black]
        return true
    }
    
    private func handleRateAlert() {
        StoreReviewHelper.incrementAppOpenedCount()
        StoreReviewHelper.checkAndAskForReview()
    }

    private func completeStorKitTransaction() {
        SwiftyStoreKit.completeTransactions(atomically: true) { purchases in
            for purchase in purchases {
                switch purchase.transaction.transactionState {
                case .purchased, .restored:
                    if purchase.needsFinishTransaction {
                        // Deliver content from server, then:
                        SwiftyStoreKit.finishTransaction(purchase.transaction)
                    }
                // Unlock content
                case .failed, .purchasing, .deferred:
                    break // do nothing
                }
            }
        }
    }
    private func setup() {
        FirebaseApp.configure()
    }

